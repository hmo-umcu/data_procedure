"""
evaluate.py
-----------
Compute segmentation quality metrics and update test_info.csv.

Two IoU metrics per image:
    1. iou_pred_annot   predicted mask vs annotation mask
    2. iou_pred_target  predicted mask vs target geometry (from draw_target_geometry.py logic)

Also computes Dice and pixel accuracy for pred vs annot.

Input
-----
    <pred_dir>/               output folder from test.py
        {stem}-pred-mask.png  predicted binary mask
        {stem}-mask.png       annotation mask (copied from original by test.py)
        {stem}.tif            original image (for well centre detection)
        {stem}.json           labelme JSON (for JSON-based well centre if available)
        test_info.csv         table generated by test.py

Usage
-----
    python evaluate.py
        --pred_dir  /scratch-shared/hmo/scaffold_images/test_predictions
        [--strand_width_mm  0.41]
        [--strand_gap_mm    2.5]
        [--no_drift]

Output
------
    Updates test_info.csv in-place with filled iou_pred_annot and iou_pred_target columns.
    Adds Dice and pixel_acc columns.
    Prints summary statistics.
"""

import argparse
import csv
import json
import re
import numpy as np
from pathlib import Path
from PIL import Image, ImageDraw


# ── geometry constants (must match draw_target_geometry.py) ───────────────────
PX_PER_MM               = 67.0
DEFAULT_STRAND_WIDTH_MM = 0.41
DEFAULT_STRAND_GAP_MM   = 2.5
N_STRANDS               = 3

ROW_STEP = (-0.15, 0.0)
COL_STEP = (0.0,   0.10)


# ── metrics ───────────────────────────────────────────────────────────────────
def binary_iou(pred, gt):
    p, g = pred > 0, gt > 0
    inter = np.logical_and(p, g).sum()
    union = np.logical_or(p,  g).sum()
    return float(inter) / float(union) if union > 0 else 0.0


def binary_dice(pred, gt):
    p, g = pred > 0, gt > 0
    inter = np.logical_and(p, g).sum()
    denom = p.sum() + g.sum()
    return float(2 * inter) / float(denom) if denom > 0 else 0.0


def pixel_accuracy(pred, gt):
    p = (pred > 0).astype(np.uint8)
    g = (gt   > 0).astype(np.uint8)
    return float((p == g).sum()) / float(g.size)


# ── target geometry (from draw_target_geometry.py) ───────────────────────────
def parse_col_row(stem):
    m = re.match(r'^(\d+)_(\d+)$', stem)
    return (int(m.group(1)), int(m.group(2))) if m else (None, None)


def get_drift_offset(stem, apply_drift=True):
    if not apply_drift:
        return 0.0, 0.0
    col, row = parse_col_row(stem)
    if col is None:
        return 0.0, 0.0
    dx = row * ROW_STEP[0] + (col - 1) * COL_STEP[0]
    dy = row * ROW_STEP[1] + (col - 1) * COL_STEP[1]
    return dx, dy


def read_well_centre_from_json(json_path):
    with open(json_path) as f:
        data = json.load(f)
    for shape in data.get('shapes', []):
        if shape.get('label') == 'well' and shape.get('shape_type') == 'circle':
            cx, cy = shape['points'][0]
            px, py = shape['points'][1]
            return float(cx), float(cy)
    return None


def detect_well_centre_auto(img_rgb):
    import cv2
    h, w = img_rgb.shape[:2]
    gray = cv2.cvtColor(img_rgb, cv2.COLOR_RGB2GRAY)
    blur = cv2.GaussianBlur(gray, (9, 9), 2)
    circles = cv2.HoughCircles(
        blur, cv2.HOUGH_GRADIENT,
        dp=1.2, minDist=300, param1=60, param2=40,
        minRadius=300, maxRadius=700,
    )
    if circles is None:
        return w / 2.0, h / 2.0
    circles = np.round(circles[0]).astype(int)
    best, best_r = None, 0
    for c in circles:
        cx, cy, r = c
        if abs(cx - w/2) < 0.35*w and abs(cy - h/2) < 0.35*h and r > best_r:
            best, best_r = c, r
    return (float(best[0]), float(best[1])) if best is not None else (w/2.0, h/2.0)


def make_target_mask(h, w, cx, cy,
                     strand_width_mm=DEFAULT_STRAND_WIDTH_MM,
                     strand_gap_mm=DEFAULT_STRAND_GAP_MM,
                     n_strands=N_STRANDS):
    half_w_px  = (strand_width_mm / 2) * PX_PER_MM
    half_span  = (n_strands - 1) / 2.0 * strand_gap_mm
    offsets_mm = [-half_span + i * strand_gap_mm for i in range(n_strands)]
    half_ext   = (half_span + strand_width_mm / 2) * PX_PER_MM

    canvas = Image.new('L', (w, h), 0)
    draw   = ImageDraw.Draw(canvas)

    for y_off_mm in offsets_mm:
        y_px = cy + y_off_mm * PX_PER_MM
        draw.rectangle([cx - half_ext, y_px - half_w_px,
                         cx + half_ext, y_px + half_w_px], fill=1)
    for x_off_mm in offsets_mm:
        x_px = cx + x_off_mm * PX_PER_MM
        draw.rectangle([x_px - half_w_px, cy - half_ext,
                         x_px + half_w_px, cy + half_ext], fill=1)

    return np.array(canvas, dtype=np.uint8)


# ── main evaluation ───────────────────────────────────────────────────────────
def evaluate(pred_dir, strand_width_mm, strand_gap_mm, apply_drift):

    pred_dir = Path(pred_dir)
    csv_path = pred_dir / 'test_info.csv'

    if not csv_path.exists():
        print(f'[ERROR] test_info.csv not found in {pred_dir}')
        print('        Run test.py first.')
        return

    # read existing CSV
    with open(csv_path, newline='') as f:
        reader = csv.DictReader(f, delimiter=';')
        fieldnames = reader.fieldnames
        rows = list(reader)

    # add new metric columns if not present
    new_fields = ['dice_pred_annot', 'pixel_acc_pred_annot']
    for nf in new_fields:
        if nf not in fieldnames:
            fieldnames.append(nf)

    print(f'Evaluating {len(rows)} predictions in {pred_dir}\n')

    results_annot  = []
    results_target = []

    for row in rows:
        stem = row['stem']
        print(f'  {stem}')

        # ── load predicted mask ───────────────────────────────────────────────
        pred_path = pred_dir / f'{stem}-pred-mask.png'
        if not pred_path.exists():
            print(f'    [SKIP] no pred mask')
            continue
        pred_arr = np.array(Image.open(pred_path))
        if pred_arr.ndim == 3:
            pred_arr = pred_arr[:, :, 0]
        pred_binary = (pred_arr > 0).astype(np.uint8)

        # ── metric 1: pred vs annotation ─────────────────────────────────────
        annot_path = pred_dir / f'{stem}-mask.png'
        if annot_path.exists():
            annot_arr = np.array(Image.open(annot_path))
            if annot_arr.ndim == 3:
                annot_arr = annot_arr[:, :, 0]
            annot_binary = (annot_arr > 0).astype(np.uint8)

            iou_pa  = binary_iou(pred_binary,  annot_binary)
            dice_pa = binary_dice(pred_binary, annot_binary)
            acc_pa  = pixel_accuracy(pred_binary, annot_binary)

            row['iou_pred_annot']      = f'{iou_pa:.4f}'
            row['dice_pred_annot']     = f'{dice_pa:.4f}'
            row['pixel_acc_pred_annot']= f'{acc_pa:.4f}'
            results_annot.append(iou_pa)
            print(f'    IoU(pred|annot)={iou_pa:.3f}  Dice={dice_pa:.3f}  Acc={acc_pa:.3f}')
        else:
            row['iou_pred_annot']      = ''
            row['dice_pred_annot']     = ''
            row['pixel_acc_pred_annot']= ''
            print(f'    [no annotation] skipping pred-vs-annot metrics')

        # ── metric 2: pred vs target geometry ────────────────────────────────
        tif_path  = pred_dir / f'{stem}.tif'
        json_path = pred_dir / f'{stem}.json'

        if not tif_path.exists():
            row['iou_pred_target'] = ''
            print(f'    [no TIF] skipping pred-vs-target metric')
            continue

        img_rgb = np.array(Image.open(tif_path).convert('RGB'))
        h, w    = img_rgb.shape[:2]

        # well centre
        if json_path.exists():
            wres = read_well_centre_from_json(json_path)
            wcx, wcy = wres if wres else detect_well_centre_auto(img_rgb)
        else:
            wcx, wcy = detect_well_centre_auto(img_rgb)

        # drift offset
        dx_mm, dy_mm = get_drift_offset(stem, apply_drift)
        cx = wcx + dx_mm * PX_PER_MM
        cy = wcy + dy_mm * PX_PER_MM

        target_mask = make_target_mask(h, w, cx, cy,
                                       strand_width_mm, strand_gap_mm)
        iou_pt = binary_iou(pred_binary, target_mask)
        row['iou_pred_target'] = f'{iou_pt:.4f}'
        results_target.append(iou_pt)
        print(f'    IoU(pred|target)={iou_pt:.3f}')

    # ── write updated CSV ─────────────────────────────────────────────────────
    with open(csv_path, 'w', newline='') as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames, delimiter=';',
                                extrasaction='ignore')
        writer.writeheader()
        writer.writerows(rows)

    # ── summary ───────────────────────────────────────────────────────────────
    print('\n── Summary ──────────────────────────────────────────')
    if results_annot:
        print(f'  IoU  (pred vs annot) :  '
              f'{np.mean(results_annot):.3f} ± {np.std(results_annot):.3f}  '
              f'[{np.min(results_annot):.3f} – {np.max(results_annot):.3f}]')
    if results_target:
        print(f'  IoU  (pred vs target):  '
              f'{np.mean(results_target):.3f} ± {np.std(results_target):.3f}  '
              f'[{np.min(results_target):.3f} – {np.max(results_target):.3f}]')
    print(f'\n✓ Updated metrics → {csv_path}')


if __name__ == '__main__':
    parser = argparse.ArgumentParser(
        description='Evaluate Cellpose-SAM predictions against annotation and target geometry.'
    )
    parser.add_argument('--pred_dir', required=True,
        help='Folder with test.py outputs (pred masks + test_info.csv)')
    parser.add_argument('--strand_width_mm', type=float,
        default=DEFAULT_STRAND_WIDTH_MM,
        help=f'Strand width mm (default: {DEFAULT_STRAND_WIDTH_MM})')
    parser.add_argument('--strand_gap_mm', type=float,
        default=DEFAULT_STRAND_GAP_MM,
        help=f'Strand gap mm (default: {DEFAULT_STRAND_GAP_MM})')
    parser.add_argument('--no_drift', action='store_true',
        help='Disable drift correction')
    args = parser.parse_args()

    evaluate(
        args.pred_dir,
        args.strand_width_mm,
        args.strand_gap_mm,
        apply_drift=not args.no_drift,
    )